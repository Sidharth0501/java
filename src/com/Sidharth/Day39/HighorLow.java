package com.Sidharth.Day39;

public class HighorLow {
    public static void main(String[] args) {
    int number = 1;
    while (number <= 10) {
        if (number > 5) {
            System.out.println(number + " High");
        } else {
            System.out.println(number + " Low");
        }
        number++;
    }
}


}
